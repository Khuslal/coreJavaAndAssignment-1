package constructor;

public class ConstructorDocs {
	/*
	 * ===========Constructor============== # Used to create object in java. #
	 * Special type of method which has same name as class name. # No return type #
	 * If there is not any user defined constructor in class , a default constructor
	 * is created by java JVM in compile time to create object.
	 * 
	 * # Two types of constructor:
	 * 		1. Default Constructor
	 * 			# Used to create object and set default values to it.
	 * 		
	 * 		# Syntax :
	 * 			class-name(){
	 * 				// set default values to object
	 * 			}
	 * 		2. Parameterized Constructor
	 * 			# Use to create object and set dynamic values to it.
	 * 		# Syntax:
	 * 			class-name(arg1, arg2, ...,argn){
	 * 				// set dynamic values to object
	 * 			}
	 */
}
