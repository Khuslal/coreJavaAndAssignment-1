package inheritance;

public class InheritanceDocs {
	/*
	 * ==========Inheritance============ 
	 * # Use for code re-usability # Process of
	 * accessing / inheriting the properties and methods of parent class to
	 * child/sub/derived class is known as inheritance.
	 * 
	 * # Also known as is-a relationship
	 * # Also called Generalization to specialization
	 * # We can inherit instance and static variables, methods.
	 * # We can't inherit private properties, method and constructors. 
	 * 
	 * # We can inherit only one class at a time.
	 * 	(i.e. multiple inheritance is not supported in java with class)
	 * 
	 * #syntax:
	 * 		child-class extends parent-class{
	 * 			// properties : parent + child
	 * 			// methods : parent + child
	 * 		}
	 */
}
